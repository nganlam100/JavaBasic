package nganlam.com.javabasic.BT3;

public class IfElse {
    private static int n = 18;
    private static int x = 8;
    public static void main(String[] args) {
        if (n == x){
            System.out.println("n == x");
        }else if (n < x){
            System.out.println("n be hon x");
        }else {
            System.out.println("n lon hon x");
        }
    }
}
