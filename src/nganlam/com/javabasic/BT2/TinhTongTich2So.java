package nganlam.com.javabasic.BT2;

public class TinhTongTich2So {

    public void Tong2SoNguyen(int number1, int number2){
        int Tong2SoNguyen;
        Tong2SoNguyen = number1+number2;
        System.out.println(Tong2SoNguyen);
    }

    public void Tich2SoThuc(double number3, double number4){
        double Tich2SoThuc;
        Tich2SoThuc = number3*number4;
        System.out.println(Tich2SoThuc);
    }
}
